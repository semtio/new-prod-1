/* =============================================
   QUIZ
   ============================================= */
const quizForm = document.getElementById('quizForm')
const quizResult = document.getElementById('quizResult')
const quizSteps = Array.from(document.querySelectorAll('.quiz__group--step'))
const quizPrev = document.getElementById('quizPrev')
const quizNext = document.getElementById('quizNext')
const quizSubmit = document.getElementById('quizSubmit')
const quizStepCurrent = document.getElementById('quizStepCurrent')
const quizStepTotal = document.getElementById('quizStepTotal')

if (quizForm && quizResult && quizSteps.length) {
  let stepIndex = 0

  if (quizStepTotal) quizStepTotal.textContent = String(quizSteps.length)

  function updateQuizStepUi() {
    quizSteps.forEach((step, index) => {
      step.classList.toggle('is-active', index === stepIndex)
    })

    if (quizStepCurrent) {
      quizStepCurrent.textContent = String(stepIndex + 1)
    }

    if (quizPrev) quizPrev.style.display = stepIndex === 0 ? 'none' : 'inline-flex'
    if (quizNext) quizNext.style.display = stepIndex === quizSteps.length - 1 ? 'none' : 'inline-flex'
    if (quizSubmit) quizSubmit.style.display = stepIndex === quizSteps.length - 1 ? 'inline-flex' : 'none'
  }

  function hasAnswerOnStep(index) {
    const step = quizSteps[index]
    if (!step) return false
    return !!step.querySelector('input[type="radio"]:checked')
  }

  if (quizNext) {
    quizNext.addEventListener('click', () => {
      if (!hasAnswerOnStep(stepIndex)) {
        quizResult.textContent = currentLang === 'ru'
          ? 'Выберите вариант ответа, чтобы перейти дальше.'
          : 'Choose an option to continue.'
        quizResult.classList.remove('quiz__result--good')
        quizResult.classList.add('quiz__result--warn')
        return
      }

      quizResult.textContent = ''
      quizResult.classList.remove('quiz__result--good', 'quiz__result--warn')
      stepIndex = Math.min(stepIndex + 1, quizSteps.length - 1)
      updateQuizStepUi()
    })
  }

  if (quizPrev) {
    quizPrev.addEventListener('click', () => {
      quizResult.textContent = ''
      quizResult.classList.remove('quiz__result--good', 'quiz__result--warn')
      stepIndex = Math.max(stepIndex - 1, 0)
      updateQuizStepUi()
    })
  }

  quizForm.addEventListener('submit', e => {
    e.preventDefault()

    const formData = new FormData(quizForm)
    const answers = ['q1', 'q2', 'q3'].map(key => formData.get(key))

    if (answers.some(answer => !answer)) {
      quizResult.textContent = currentLang === 'ru'
        ? 'Пожалуйста, выберите ответы на все 3 вопроса.'
        : 'Please answer all 3 questions.'
      quizResult.classList.remove('quiz__result--good')
      quizResult.classList.add('quiz__result--warn')
      return
    }

    const score = answers.filter(answer => answer === 'yes').length

    if (score >= 2) {
      quizResult.textContent = currentLang === 'ru'
        ? 'Предварительно: у вас хорошие шансы на penalty relief. Рекомендуем отправить запрос на персональную оценку.'
        : 'Preliminary result: you likely have a good chance for penalty relief. We recommend requesting a personal assessment.'
      quizResult.classList.remove('quiz__result--warn')
      quizResult.classList.add('quiz__result--good')
      return
    }

    quizResult.textContent = currentLang === 'ru'
      ? 'Предварительно: ситуация требует детального разбора. Напишите нам, и мы предложим стратегию ответа в IRS.'
      : 'Preliminary result: your case needs a detailed review. Contact us and we will propose an IRS response strategy.'
    quizResult.classList.remove('quiz__result--good')
    quizResult.classList.add('quiz__result--warn')
  })

  updateQuizStepUi()
}


/* =============================================
   FOOTER THEME (dark on last slide)
   ============================================= */
function updateFooterTheme(sw) {
  const isLast = sw.activeIndex === sw.slides.length - 1
  const isCompact = sw.activeIndex > 0

  const header = document.querySelector('.header')

  document.body.classList.toggle('slide-last', isLast)
  document.body.classList.toggle('header-compact', isCompact)
  header.classList.toggle('header--scrolled', isCompact)
  header.classList.toggle('header--compact', isCompact)
}


/* =============================================
   COUNT-UP ANIMATION (slide 2 — penalty stats)
   ============================================= */
function runCountUp() {
  document.querySelectorAll('[data-count]').forEach(el => {
    const target   = parseInt(el.dataset.count, 10)
    const prefix   = el.dataset.prefix || ''
    const duration = 1600
    const startAt  = performance.now()

    function tick(now) {
      const elapsed  = now - startAt
      const progress = Math.min(elapsed / duration, 1)
      // Ease out cubic
      const eased = 1 - Math.pow(1 - progress, 3)
      const value = Math.round(eased * target)

      el.textContent = prefix + value.toLocaleString('en-US')

      if (progress < 1) requestAnimationFrame(tick)
    }

    requestAnimationFrame(tick)
  })
}


